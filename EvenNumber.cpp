#include <iostream>
using namespace std;

int main()
{
    int number = 10;
    
    if (number % 2 == 0) // if a number is even
    {
        cout << "The number is even." << endl;
    }
    else // if a number is not even
    {
        cout << "The number is odd." << endl;
    }
    
    return 0;
}
